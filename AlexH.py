# Define the color codes
colors = [
    "\033[91m",  # Red
    "\033[92m",  # Green
    "\033[93m",  # Yellow
    "\033[94m",  # Blue
    "\033[95m",  # Magenta
    "\033[96m",  # Cyan
    "\033[97m",  # White
    "\033[0m"    # Reset
]

def clear_screen():
    # Clear the terminal screen
    os.system('cls' if os.name == 'nt' else 'clear')

def print_text_and_art():
    while True:
        for color in colors:
            clear_screen()
            # Print the ASCII art text and Walter White ASCII art in the current color
            sys.stdout.write(color + ascii_text + "\n" + walter_white + "\033[0m")
            sys.stdout.flush()
            time.sleep(1)  # Adjust the speed as needed

if __name__ == "__main__":
    print_text_and_art()
