# Function to print bullet animation shooting down
def bullet_animation():
    frames = [
        "•",
        "•\n•",
        "•\n•\n•",
        "•\n•\n•\n•",
        "•\n•\n•\n•\n•",
    ]
    for frame in frames:
        clear_screen()
        print(frame)
        time.sleep(0.2)

